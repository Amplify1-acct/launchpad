// ─── Main handler ──────────────────────────────────────────────────────────

export async function POST(request: Request) {
  const body = await request.json();
  const { business_id } = body;
  if (!business_id) {
    return NextResponse.json({ error: "business_id required" }, { status: 400 });
  }

  if (!GH_TOKEN || !VERCEL_TOKEN) {
    return NextResponse.json({ error: "GITHUB_TOKEN and VERCEL_TOKEN env vars required" }, { status: 500 });
  }

  const supabase = createAdminClient();

  // Fetch business + website data
  const { data: business, error: bizErr } = await supabase
    .from("businesses")
    .select("*")
    .eq("id", business_id)
    .single();

  if (bizErr || !business) {
    return NextResponse.json({ error: "Business not found" }, { status: 404 });
  }

  const { data: website } = await supabase
    .from("websites")
    .select("*")
    .eq("business_id", business_id)
    .single();

  // Update status to generating
  await supabase
    .from("websites")
    .update({ status: "generating" })
    .eq("business_id", business_id);

  try {
    // Build slug for repo/project names
    const slug = business.name
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-|-$/g, "")
      .slice(0, 40);

    const repoName = `exsisto-site-${slug}`;
    const projectName = `exsisto-${slug}`;

    // Fetch HTML from chosen Stitch screen if provided
    let html = "";
    const screenId = body.screen_id;
    const stitchProjectId = body.project_id || website?.stitch_project_id;
    if (screenId && stitchProjectId) {
      try {
        const codeRes = await fetch(
          `https://stitch.googleapis.com/v1alpha/projects/${stitchProjectId}/screens/${screenId}/code`,
          { headers: { Authorization: `Bearer ${process.env.STITCH_API_KEY}` } }
        );
        if (codeRes.ok) {
          const codeData = await codeRes.json();
          if (codeData.downloadUrl) {
            const r = await fetch(codeData.downloadUrl);
            html = await r.text();
          } else if (codeData.html) {
            html = codeData.html;
          }
        }
      } catch (e) { console.warn("Stitch fetch failed, using fallback:", e); }
    }
    if (!html) {
      html = (website?.custom_html as string) || buildSiteHTML(business, website || {});
    }

    // Push to GitHub
    await createRepo(repoName);
    await pushFiles(repoName, {
      "index.html": html,
    });

    // Deploy to Vercel
    const { deploymentId, url } = await deployToVercel(repoName, projectName);

    // Wait for deployment to be ready (up to 2 min)
    let liveUrl: string;
    try {
      liveUrl = await waitForDeployment(deploymentId);
    } catch {
      // Use the URL even if we timed out — it's usually ready
      liveUrl = url;
    }

    // Save live URL back to Supabase
    await supabase
      .from("websites")
      .update({
        status: "live",
        vercel_url: liveUrl,
        deployed_at: new Date().toISOString(),
      })
      .eq("business_id", business_id);

    await supabase
      .from("businesses")
      .update({ website_url: liveUrl })
      .eq("id", business_id);

    return NextResponse.json({ success: true, url: liveUrl, deploymentId });
  } catch (error: any) {
    await supabase
      .from("websites")
      .update({ status: "error" })
      .eq("business_id", business_id);

    console.error("Deploy error:", error);
    return NextResponse.json({ error: error.message }, { status: 500 });
  }
}
