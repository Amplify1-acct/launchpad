// ─── GitHub helpers ────────────────────────────────────────────────────────

async function ghFetch(path: string, method = "GET", body?: object) {
  const res = await fetch(`https://api.github.com${path}`, {
    method,
    headers: {
      Authorization: `token ${GH_TOKEN}`,
      Accept: "application/vnd.github+json",
      "Content-Type": "application/json",
      "X-GitHub-Api-Version": "2022-11-28",
    },
    body: body ? JSON.stringify(body) : undefined,
  });
  if (!res.ok) {
    const err = await res.text();
    throw new Error(`GitHub ${method} ${path} → ${res.status}: ${err}`);
  }
  return res.json();
}

async function createRepo(repoName: string) {
  try {
    // Try to delete if exists (clean slate)
    await ghFetch(`/repos/${GH_ORG}/${repoName}`, "DELETE");
    await new Promise((r) => setTimeout(r, 2000));
  } catch {}

  return ghFetch(`/orgs/${GH_ORG}/repos`, "POST", {
    name: repoName,
    private: false,
    auto_init: false,
    description: "Generated by Exsisto",
  });
}

async function pushFiles(
  repoName: string,
  files: Record<string, string>
) {
  // Create blob for each file
  const blobs = await Promise.all(
    Object.entries(files).map(async ([path, content]) => {
      const blob = await ghFetch(`/repos/${GH_ORG}/${repoName}/git/blobs`, "POST", {
        content: Buffer.from(content).toString("base64"),
        encoding: "base64",
      });
      return { path, sha: blob.sha };
    })
  );

  // Create tree
  const tree = await ghFetch(`/repos/${GH_ORG}/${repoName}/git/trees`, "POST", {
    tree: blobs.map(({ path, sha }) => ({
      path,
      mode: "100644",
      type: "blob",
      sha,
    })),
  });

  // Create commit
  const commit = await ghFetch(`/repos/${GH_ORG}/${repoName}/git/commits`, "POST", {
    message: "Initial site generated by Exsisto",
    tree: tree.sha,
    parents: [],
  });

  // Create main branch
  await ghFetch(`/repos/${GH_ORG}/${repoName}/git/refs`, "POST", {
    ref: "refs/heads/main",
    sha: commit.sha,
  });

  return commit.sha;
}

// ─── Vercel helpers ────────────────────────────────────────────────────────

async function vercelFetch(path: string, method = "GET", body?: object) {
  const res = await fetch(`https://api.vercel.com${path}`, {
    method,
    headers: {
      Authorization: `Bearer ${VERCEL_TOKEN}`,
      "Content-Type": "application/json",
    },
    body: body ? JSON.stringify(body) : undefined,
  });
  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Vercel ${method} ${path} → ${res.status}: ${err}`);
  }
  return res.json();
}

async function deployToVercel(repoName: string, projectName: string) {
  // Create Vercel project linked to GitHub repo
  let project;
  try {
    project = await vercelFetch("/v9/projects", "POST", {
      name: projectName,
      gitRepository: {
        type: "github",
        repo: `${GH_ORG}/${repoName}`,
      },
      framework: null, // static site
    });
  } catch (e: any) {
    // Project may already exist — fetch it
    const list = await vercelFetch(`/v9/projects/${projectName}`);
    project = list;
  }

  // Trigger a deployment
  const deployment = await vercelFetch("/v13/deployments", "POST", {
    name: projectName,
    gitSource: {
      type: "github",
      org: GH_ORG,
      repo: repoName,
      ref: "main",
    },
    projectSettings: {
      framework: null,
      outputDirectory: ".",
    },
  });

  return {
    deploymentId: deployment.id,
    url: `https://${deployment.url}`,
    projectId: project.id,
  };
}

async function waitForDeployment(deploymentId: string, maxWaitMs = 120000): Promise<string> {
  const start = Date.now();
  while (Date.now() - start < maxWaitMs) {
    await new Promise((r) => setTimeout(r, 5000));
    const dep = await vercelFetch(`/v13/deployments/${deploymentId}`);
    if (dep.readyState === "READY") {
      return `https://${dep.url}`;
    }
    if (dep.readyState === "ERROR" || dep.readyState === "CANCELED") {
      throw new Error(`Deployment ${dep.readyState}`);
    }
  }
  throw new Error("Deployment timed out");
}

